int	ft_sqrt(int nb)
{
	int	sqrt;
	int	odd;

	odd = 1;
	sqrt = 0;
	while (nb > 0)
	{
		nb -= odd;
		odd += 2;
		sqrt++;
	}
	if (nb == 0)
		return (sqrt);
	return (0);
}
